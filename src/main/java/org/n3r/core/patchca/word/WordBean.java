package org.n3r.core.patchca.word;

public class WordBean {

    private String word;
    private String answer;

    public WordBean(String word, String answer) {
        this.word = word;
        this.answer = answer;
    }

    public String getWord() {
        return word;
    }

    public String getAnswer() {
        return answer;
    }

}
